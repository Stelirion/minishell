#include "../libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	size_t			i;
	unsigned char	chr;
	unsigned char	*s;

	i = -1;
	chr = c;
	s = (unsigned char *)str;
	while (++i < n)
		if (s[i] == chr)
			return ((void *)str + i);
	return (NULL);
}
